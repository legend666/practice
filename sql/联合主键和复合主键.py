'今天获取元数据的时候，程序输出的主键名和主键列名，我就懵逼了。。。不知道有啥区别了，'
'之前都是用power画的，其实就像建立一个表，你得给表起个名吧，表的内容另说，'
'主键名就是建立主键的时候的名字。'
'要指定一列是主键，这个列的名就是主键名了。'
"还有一个概念就是联合主键和复合主键。"
'建立主键的时候只能建立一个，但是可以指定不止一个列为主键，这个就叫做复合主键'
'联合主键就是一条信息中，包含其他表的主键'
'区别就是联合主键必须至少两张表及以上'
